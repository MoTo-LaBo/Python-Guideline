# 比較演算子 (relational operator): > , < , >= , <= , !=
print(5 > 3)  # Ture
print(2.0 < 2.0)  # False

# Ture 英語の順番だと思っておけば = の位置は迷わない
# small or Then equal too : 小さくても、同じくらいでも
print(5 <= 5)  #True
print(5 >= 3)  #True
print(5 == 5)  #True
print(5 != 3)  #True : = はnot のような意味

# 数値型（Numeric）: integer(整数),float(小数)
print(1 == 1.0)  #True
print(1 == "1")  #False
print("1" == "1")  #True


